import UIKit

class ProjectKeysViewController: UITableViewController {

    var projectKeys: [ProjectKey]!
    weak var delegate: ProjectKeyDelegate?
    
    override func viewWillAppear(_ animated: Bool) {
        super.viewWillAppear(animated)
        // Reset the selected organization index, so that no activity indicator is shown.
        tableView.tag = -1
        tableView.reloadData()
    }

    // MARK: - Table view data source

    override func numberOfSections(in tableView: UITableView) -> Int {
        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return projectKeys.count
    }
    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "value", for: indexPath)
        let description = projectKeys[indexPath.row].description
        cell.textLabel?.text = description.isEmpty ? "No description" : description
        cell.detailTextLabel?.text = projectKeys[indexPath.row].shortened
        return cell
    }
    
    override func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath) {
        tableView.deselectRow(at: indexPath, animated: true)
        
        let projectKey = projectKeys[indexPath.row]
        delegate?.didSelectProjectKey(projectKey)
        dismiss(animated: true)
    }

}
